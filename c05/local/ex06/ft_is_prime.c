int	ft_is_prime(int nb)
{
	int	mod;

	if (nb <= 1)
	{
		return (0);
	}
	if (nb == 2)
	{
		return (1);
	}
	if (nb % 2 == 0)
	{
		return (0);
	}
	mod = 3;
	while (mod <= nb / 2)
	{
		if (nb % mod == 0)
		{
			return (0);
		}
		mod += 2;
	}
	return (1);
}
