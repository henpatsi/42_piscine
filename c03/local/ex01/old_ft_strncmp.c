int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int	i;

	i = n;
	while (*s1 != 0 && *s1 != 0 && i > 0)
	{
		if (*s1 != *s2)
		{
			return (*s1 - *s2);
		}
		s1++;
		s2++;
		i--;
	}
	if (*s1 != *s2 && i > 0)
	{
		return (*s1 - *s2);
	}
	return (0);
}
